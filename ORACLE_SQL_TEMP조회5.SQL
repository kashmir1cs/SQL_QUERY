--연도별 평균
SELECT SUBSTR(A.STD_DE, 1,4) AS YEAR
	  ,ROUND(AVG(AVG_TEMPER),2) AS AVG_TEMPER
  FROM TB_TEMPER A
 WHERE A.AREA_CD ='108' --지역코드 : 서울특별시
 GROUP BY SUBSTR(A.STD_DE,1,4)
 ORDER BY SUBSTR(A.STD_DE,1,4) ;
 

--1907년도 데이터 조회 
SELECT *
FROM TB_TEMPER A
WHERE STD_DE LIKE '1907%'
ORDER BY A.STD_DE;
